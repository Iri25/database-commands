FROM PRODUSE P
JOIN STRUCTURA S ON P.COD = S.COD
WHERE S.COD IN ( -- inregistrarile care au codul din STRUCTURA in subinterogare
    SELECT COD     -- subinterogare care selecteaza toate codurile din STRUCTURA
    FROM STRUCTURA -- care au codul PRODUSE din categoria "Diverse"
    WHERE CODP IN (
        SELECT COD   -- subinterogare care selecteaza toate inregistrarile din 
        FROM PRODUSE -- PRODUSE care au codul din subinterogarea anterioara
        WHERE DENUMIRE = 'Diverse'
    )
);

